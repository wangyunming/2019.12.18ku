package com.haoyin.image.entity;

import java.util.Date;



/**
 * @author Summer
 *
 *         2016年5月20日
 */
public class UserLoginQuery extends Paginator {

	private static final long serialVersionUID = 1L;

	/** 开始时间 */
	private Date startDate;

	/** 结束时间 */
	private Date endDate;

	/** 关键字 */
	private String keywords;

	/** 登录状态 */
	private Integer loginStatus;

	/**
	 * @return the {@link #startDate}
	 */
	public Date getStartDate() {
		return startDate;
	}

	/**
	 * @param startDate
	 *            the {@link #startDate} to set
	 */
	public void setStartDate(Date startDate) {
		this.startDate = startDate;
	}

	/**
	 * @return the {@link #endDate}
	 */
	public Date getEndDate() {
		return endDate;
	}

	/**
	 * @param endDate
	 *            the {@link #endDate} to set
	 */
	public void setEndDate(Date endDate) {
		this.endDate = endDate;
	}

	/**
	 * @return the {@link #keywords}
	 */
	public String getKeywords() {
		return keywords;
	}

	/**
	 * @param keywords
	 *            the {@link #keywords} to set
	 */
	public void setKeywords(String keywords) {
		this.keywords = keywords;
	}

	/**
	 * @return the {@link #loginStatus}
	 */
	public Integer getLoginStatus() {
		return loginStatus;
	}

	/**
	 * @param loginStatus
	 *            the {@link #loginStatus} to set
	 */
	public void setLoginStatus(Integer loginStatus) {
		this.loginStatus = loginStatus;
	}

}
