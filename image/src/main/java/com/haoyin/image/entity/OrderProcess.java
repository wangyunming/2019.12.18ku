package com.haoyin.image.entity;

import java.io.Serializable;
import java.util.Date;

public class OrderProcess implements Serializable{
	private static final long serialVersionUID = 1L;

	/** 订单 ID */
	private Long orderId;

	/** 订单处理状态 */
	private Integer orderProcessStatus;

	/** 处理内容 */
	private String process;

	/** 处理时间 */
	private Date processTime;

	/** 处理人 ID */
	private Long processUid;

	/** 处理人 */
	private String processUser;

	/** 处理人身份( 0: 会员; 1: 后台用户) */
	private Integer status;

	/**
	 * @return the {@link #orderId}
	 */
	public Long getOrderId() {
		return orderId;
	}

	/**
	 * @param orderId
	 *            the {@link #orderId} to set
	 */
	public void setOrderId(Long orderId) {
		this.orderId = orderId;
	}

	/**
	 * @return the {@link #orderProcessStatus}
	 */
	public Integer getOrderProcessStatus() {
		return orderProcessStatus;
	}

	/**
	 * @param orderProcessStatus
	 *            the {@link #orderProcessStatus} to set
	 */
	public void setOrderProcessStatus(Integer orderProcessStatus) {
		this.orderProcessStatus = orderProcessStatus;
	}

	/**
	 * @return the {@link #process}
	 */
	public String getProcess() {
		return process;
	}

	/**
	 * @param process
	 *            the {@link #process} to set
	 */
	public void setProcess(String process) {
		this.process = process;
	}

	/**
	 * @return the {@link #processTime}
	 */
	public Date getProcessTime() {
		return processTime;
	}

	/**
	 * @param processTime
	 *            the {@link #processTime} to set
	 */
	public void setProcessTime(Date processTime) {
		this.processTime = processTime;
	}

	/**
	 * @return the {@link #processUid}
	 */
	public Long getProcessUid() {
		return processUid;
	}

	/**
	 * @param processUid
	 *            the {@link #processUid} to set
	 */
	public void setProcessUid(Long processUid) {
		this.processUid = processUid;
	}

	/**
	 * @return the {@link #processUser}
	 */
	public String getProcessUser() {
		return processUser;
	}

	/**
	 * @param processUser
	 *            the {@link #processUser} to set
	 */
	public void setProcessUser(String processUser) {
		this.processUser = processUser;
	}

	/**
	 * @return the {@link #status}
	 */
	public Integer getStatus() {
		return status;
	}

	/**
	 * @param status
	 *            the {@link #status} to set
	 */
	public void setStatus(Integer status) {
		this.status = status;
	}

	@Override
	public String toString() {
		return new StringBuilder("com.haoyin.crm.model.order.OrderProcessDO =[").append("orderId = ").append(orderId)
				.append(", orderProcessStatus = ").append(orderProcessStatus).append(", process = ").append(process)
				.append(", processTime = ").append(processTime).append(", processUid = ").append(processUid)
				.append(", processUser = ").append(processUser).append(", status = ").append(status).toString();
	}
}
