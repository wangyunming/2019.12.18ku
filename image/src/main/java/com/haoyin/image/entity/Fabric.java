package com.haoyin.image.entity;

import java.io.Serializable;
import java.util.List;

public class Fabric  implements Serializable{
	
	private static final long serialVersionUID = 1L;

	private Long id;
	
	/** 面料名称 **/
	private String name;
	
	/** 成分 **/
	private List<List<String>> component;
	
	/** 备注 **/
	private String remarks;
	
	/** 备注 **/
	private List<String> remarksList;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public List<List<String>> getComponent() {
		return component;
	}

	public void setComponent(List<List<String>> component) {
		this.component = component;
	}

	public String getRemarks() {
		return remarks;
	}

	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}

	public List<String> getRemarksList() {
		return remarksList;
	}

	public void setRemarksList(List<String> remarksList) {
		this.remarksList = remarksList;
	}
	
	
	
	

}
