package com.haoyin.image.entity;

/**
 * System user roles
 * 
 * @author Summer
 *
 *         2016年3月10日
 */
public class UserRole implements java.io.Serializable {

	private static final long serialVersionUID = 1L;

	/** 用户ID */
	private Long userId;

	/** 部门ID */
	private Long deptId;

	/** 角色ID */
	private Long roleId;

	/** 用户名 */
	private String username;

	/** 角色名称 */
	private String roleName;
	
	/** position level */
	private Integer positionLevel;

	/**
	 * @return the {@link #userId}
	 */
	public Long getUserId() {
		return userId;
	}

	/**
	 * @param userId
	 *            the {@link #userId} to set
	 */
	public void setUserId(Long userId) {
		this.userId = userId;
	}

	/**
	 * @return the {@link #deptId}
	 */
	public Long getDeptId() {
		return deptId;
	}

	/**
	 * @param deptId
	 *            the {@link #deptId} to set
	 */
	public void setDeptId(Long deptId) {
		this.deptId = deptId;
	}

	/**
	 * @return the {@link #roleId}
	 */
	public Long getRoleId() {
		return roleId;
	}

	/**
	 * @param roleId
	 *            the {@link #roleId} to set
	 */
	public void setRoleId(Long roleId) {
		this.roleId = roleId;
	}

	/**
	 * @return the {@link #username}
	 */
	public String getUsername() {
		return username;
	}

	/**
	 * @param username
	 *            the {@link #username} to set
	 */
	public void setUsername(String username) {
		this.username = username;
	}

	/**
	 * @return the {@link #roleName}
	 */
	public String getRoleName() {
		return roleName;
	}

	/**
	 * @param roleName
	 *            the {@link #roleName} to set
	 */
	public void setRoleName(String roleName) {
		this.roleName = roleName;
	}

	public Integer getPositionLevel() {
		return positionLevel;
	}

	public void setPositionLevel(Integer positionLevel) {
		this.positionLevel = positionLevel;
	}

	@Override
	public String toString() {
		return new StringBuilder(UserRole.class.getSimpleName()).append("\n userId=").append(userId).append(", deptId=").append(deptId).append(", roleId=")
				.append(roleId).append(", username=").append(username).append(", roleName=").append(roleName).toString();
	}
}
