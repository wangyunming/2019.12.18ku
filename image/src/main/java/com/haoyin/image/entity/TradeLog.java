package com.haoyin.image.entity;

import java.io.Serializable;
import java.util.Date;



public class TradeLog implements Serializable {

	private static final long serialVersionUID = 1L;

	/** ID */
	private Long id;

	/** 订单 ID */
	private String orderId;

	/** 订单编号 */
	private String orderNo;

	/** 交易号, 由第三方支付系统提供 */
	private String tradeNo;

	/** 交易或订单名称 */
	private String subject;

	/** 交易总金额 */
	private String totalFee;

	/** 交易创建时间 */
	private String tradeCreateTime;

	/** 卖家帐号 */
	private String seller;

	/** 买家帐号 */
	private String buyer;

	/** 用户 ID */
	private Long userId;

	/** 买家用户名 */
	private String buyerName;

	/** 交易类型 (1: 商品购买; 2: 充值(服务购买)) */
	private String tradeType;

	/** 支付时间 */
	private String paymentTime;

	/**
	 * 支付方式 (1: 支付宝; 2:余额; 3:银行转帐; 4: 现金; 5:支票)
	 * <p>
	 * 帐户充值时,不存在 2
	 * </p>
	 */
	private String payMode;

	/** 银行编码 */
	private String bankCode;

	/** 交易状态 */
	private String tradeStatus;

	/** 交易备注 */
	private String remark;

	/** 记录创建时间 */
	private Date createGmt;

	/** 状态 1: 有效; 2:退款 */
	private Integer status;

	/**
	 * @return <code>the {@link #id}</code>
	 */
	public Long getId() {
		return id;
	}

	/**
	 * @param id
	 *            <code>the {@link #id} to set</code>
	 */
	public void setId(Long id) {
		this.id = id;
	}

	/**
	 * @return the {@link orderId}
	 */
	public String getOrderId() {
		return orderId;
	}

	/**
	 * @param orderId
	 *            the {@link orderId} to set
	 */
	public void setOrderId(String orderId) {
		this.orderId = orderId;
	}

	/**
	 * @return <code>the {@link #orderNo}</code>
	 */
	public String getOrderNo() {
		return orderNo;
	}

	/**
	 * @param orderNo
	 *            <code>the {@link #orderNo} to set</code>
	 */
	public void setOrderNo(String orderNo) {
		this.orderNo = orderNo;
	}

	/**
	 * @return <code>the {@link #tradeNo}</code>
	 */
	public String getTradeNo() {
		return tradeNo;
	}

	/**
	 * @param tradeNo
	 *            <code>the {@link #tradeNo} to set</code>
	 */
	public void setTradeNo(String tradeNo) {
		this.tradeNo = tradeNo;
	}

	/**
	 * @return <code>the {@link #subject}</code>
	 */
	public String getSubject() {
		return subject;
	}

	/**
	 * @param subject
	 *            <code>the {@link #subject} to set</code>
	 */
	public void setSubject(String subject) {
		this.subject = subject;
	}

	/**
	 * @return the {@link #paymentTime}
	 */
	public String getPaymentTime() {
		return paymentTime;
	}

	/**
	 * @param paymentTime
	 *            the {@link #paymentTime} to set
	 */
	public void setPaymentTime(String paymentTime) {
		this.paymentTime = paymentTime;
	}

	/**
	 * @return the {@link #tradeCreateTime}
	 */
	public String getTradeCreateTime() {
		return tradeCreateTime;
	}

	/**
	 * @param tradeCreateTime
	 *            the {@link #tradeCreateTime} to set
	 */
	public void setTradeCreateTime(String tradeCreateTime) {
		this.tradeCreateTime = tradeCreateTime;
	}

	/**
	 * @return <code>the {@link #totalFee}</code>
	 */
	public String getTotalFee() {
		return totalFee;
	}

	/**
	 * @param totalFee
	 *            <code>the {@link #totalFee} to set</code>
	 */
	public void setTotalFee(String totalFee) {
		this.totalFee = totalFee;
	}

	/**
	 * @return the {@link #seller}
	 */
	public String getSeller() {
		return seller;
	}

	/**
	 * @param seller
	 *            the {@link #seller} to set
	 */
	public void setSeller(String seller) {
		this.seller = seller;
	}

	/**
	 * @return the {@link #userId}
	 */
	public Long getUserId() {
		return userId;
	}

	/**
	 * @param userId
	 *            the {@link #userId} to set
	 */
	public void setUserId(Long userId) {
		this.userId = userId;
	}

	/**
	 * @return the {@link #buyer}
	 */
	public String getBuyer() {
		return buyer;
	}

	/**
	 * @param buyer
	 *            the {@link #buyer} to set
	 */
	public void setBuyer(String buyer) {
		this.buyer = buyer;
	}

	/**
	 * @return the {@link #buyerName}
	 */
	public String getBuyerName() {
		return buyerName;
	}

	/**
	 * @param buyerName
	 *            the {@link #buyerName} to set
	 */
	public void setBuyerName(String buyerName) {
		this.buyerName = buyerName;
	}

	/**
	 * @return the {@link #tradeStatus}
	 */
	public String getTradeStatus() {
		return tradeStatus;
	}

	/**
	 * @param tradeStatus
	 *            the {@link #tradeStatus} to set
	 */
	public void setTradeStatus(String tradeStatus) {
		this.tradeStatus = tradeStatus;
	}

	/**
	 * @return the {@link #payMode}
	 */
	public String getPayMode() {
		return payMode;
	}

	/**
	 * @param payMode
	 *            the {@link #payMode} to set
	 */
	public void setPayMode(String payMode) {
		this.payMode = payMode;
	}

	/**
	 * @return the {@link #bankCode}
	 */
	public String getBankCode() {
		return bankCode;
	}

	/**
	 * @param bankCode
	 *            the {@link #bankCode} to set
	 */
	public void setBankCode(String bankCode) {
		this.bankCode = bankCode;
	}

	/**
	 * @return the {@link tradeType}
	 */
	public String getTradeType() {
		return tradeType;
	}

	/**
	 * @param tradeType
	 *            the {@link tradeType} to set
	 */
	public void setTradeType(String tradeType) {
		this.tradeType = tradeType;
	}

	/**
	 * @return the {@link #remark}
	 */
	public String getRemark() {
		return remark;
	}

	/**
	 * @param remark
	 *            the {@link #remark} to set
	 */
	public void setRemark(String remark) {
		this.remark = remark;
	}

	/**
	 * @return the {@link #createGmt}
	 */
	public Date getCreateGmt() {
		return createGmt;
	}

	/**
	 * @param createGmt
	 *            the {@link #createGmt} to set
	 */
	public void setCreateGmt(Date createGmt) {
		this.createGmt = createGmt;
	}

	/**
	 * @return the status
	 */
	public Integer getStatus() {
		return status;
	}

	/**
	 * @param status
	 *            the status to set
	 */
	public void setStatus(Integer status) {
		this.status = status;
	}

	@Override
	public String toString() {
		return new StringBuilder(getClass().getSimpleName()).append(" [id = ").append(id).append(", orderNo = ")
				.append(orderNo).append(", tradeNo = ").append(tradeNo).append(", subject = ").append(subject)
				.append(", totalFee = ").append(totalFee).append(", seller=").append(seller).append(", user=(")
				.append("id=").append(userId).append(", name=").append(buyerName).append(", account=").append(buyer)
				.append("), paymentTime = ").append(paymentTime).append(", tradeCreateTime=").append(tradeCreateTime)
				.append(", tradeStatus = ").append(tradeStatus).append(", payMode=").append(payMode)
				.append(", bankCode=").append(bankCode).append(", tradeType = ").append(tradeType)
				.append(", remark = ").append(remark).append(", create_gmt=").append(createGmt).append(", status = ")
				.append(status).append("]").toString();
	}
}