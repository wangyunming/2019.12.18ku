public class RoleQuery extends Paginator {

	private static final long serialVersionUID = 1L;

	/** Key words */
	private String keywords;

	/** 父级ID */
	private Long parentId;

	/** 部门 ID */
	private Long deptId;

	/** 主体ID */
	private Long subjectId;

	/** 部门名称 */
	private String deptName;

	/** 状态 */
	private Integer status;
	
	/**职位id*/
	private List<Integer> roleIdS;
	
	private List<String> positionS;
	/**等级*/
	private Integer lervelNum;
	
	
	public List<String> getPositionS() {
		return positionS;
	}

	public void setPositionS(List<String> positionS) {
		this.positionS = positionS;
	}

	public Integer getLervelNum() {
		return lervelNum;
	}

	public void setLervelNum(Integer lervelNum) {
		this.lervelNum = lervelNum;
	}

	/**
	 * @return the {@link #keywords}
	 */
	public String getKeywords() {
		return keywords;
	}

	/**
	 * @param keywords
	 *            the {@link #keywords} to set
	 */
	public void setKeywords(String keywords) {
		this.keywords = keywords;
	}

	/**
	 * @return the {@link #parentId}
	 */
	public Long getParentId() {
		return parentId;
	}

	/**
	 * @param parentId
	 *            the {@link #parentId} to set
	 */
	public void setParentId(Long parentId) {
		this.parentId = parentId;
	}

	/**
	 * @return the {@link #deptId}
	 */
	public Long getDeptId() {
		return deptId;
	}

	/**
	 * @param deptId
	 *            the {@link #deptId} to set
	 */
	public void setDeptId(Long deptId) {
		this.deptId = deptId;
	}

	/**
	 * @return the {@link #subjectId}
	 */
	public Long getSubjectId() {
		return subjectId;
	}

	/**
	 * @param subjectId
	 *            the {@link #subjectId} to set
	 */
	public void setSubjectId(Long subjectId) {
		this.subjectId = subjectId;
	}

	/**
	 * @return the {@link #deptName}
	 */
	public String getDeptName() {
		return deptName;
	}

	/**
	 * @param deptName
	 *            the {@link #deptName} to set
	 */
	public void setDeptName(String deptName) {
		this.deptName = deptName;
	}

	/**
	 * @return the {@link #status}
	 */
	public Integer getStatus() {
		return status;
	}

	/**
	 * @param status
	 *            the {@link #status} to set
	 */
	public void setStatus(Integer status) {
		this.status = status;
	}

	public List<Integer> getRoleIdS() {
		return roleIdS;
	}

	public void setRoleIdS(List<Integer> roleIdS) {
		this.roleIdS = roleIdS;
	}
	
}
